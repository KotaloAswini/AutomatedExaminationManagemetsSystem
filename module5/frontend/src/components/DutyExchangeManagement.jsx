import React, { useState } from 'react';
import { useNavigate } from 'react-router-dom';
import './DutyExchangeManagement.css';

const DutyExchangeManagement = () => {
    const navigate = useNavigate();

    // State for exchanges
    const [exchanges, setExchanges] = useState([]);

    // Fetch data from backend on component mount
    React.useEffect(() => {
        const fetchExchanges = async () => {
            try {
                const response = await fetch('http://localhost:8080/api/duty-exchange');
                if (response.ok) {
                    const data = await response.json();
                    // Transformation might be needed if backend field names differ slightly
                    // Assuming backend returns matching structure or we map it here
                    const mappedData = data.map((item, index) => ({
                        id: item.id,
                        readableId: index + 1,
                        requestedBy: item.allotted?.name || 'Unknown',
                        allottedDuty: `${item.allotted?.dutyType || ''} - ${item.allotted?.date || ''}`,
                        exchangeWith: item.exchangedWith?.name || 'Unknown',
                        exchangeDuty: `${item.exchangedWith?.dutyType || ''} - ${item.exchangedWith?.date || ''}`,
                        status: item.status?.toLowerCase() || 'pending',
                        date: item.requestDate || '',
                        hodApproval: item.hodApproval?.toLowerCase() || 'pending'
                    }));
                    setExchanges(mappedData);
                } else {
                    console.error('Failed to fetch exchanges');
                }
            } catch (error) {
                console.error('Error fetching exchanges:', error);
            }
        };

        fetchExchanges();
    }, []);

    const [filter, setFilter] = useState('all'); // all, pending, approved, rejected
    const [searchQuery, setSearchQuery] = useState('');
    const [showModal, setShowModal] = useState(false);
    const [showNotifications, setShowNotifications] = useState(false);
    const [selectedExchange, setSelectedExchange] = useState(null);
    const [readNotificationIds, setReadNotificationIds] = useState(new Set());
    const [activeTab, setActiveTab] = useState('All');

    const [showOptions, setShowOptions] = useState(false);

    // Disable background scrolling when modal is open
    React.useEffect(() => {
        const content = document.querySelector('.management-content');
        if (showModal) {
            document.body.style.overflow = 'hidden';
            if (content) content.style.overflowY = 'hidden';
        } else {
            document.body.style.overflow = 'unset';
            if (content) content.style.overflowY = 'auto';
        }

        // Cleanup function
        return () => {
            document.body.style.overflow = 'unset';
            if (content) content.style.overflowY = 'auto';
        };
    }, [showModal]);

    const handleApprove = async (id) => {
        try {
            const response = await fetch(`http://localhost:8080/api/duty-exchange/${id}/approve`, {
                method: 'POST',
            });
            if (response.ok) {
                setExchanges(exchanges.map(ex =>
                    ex.id === id ? { ...ex, status: 'approved', hodApproval: 'approved' } : ex
                ));
                showNotification('Exchange request approved successfully!', 'success');
            } else {
                showNotification('Failed to approve request', 'error');
            }
        } catch (error) {
            console.error('Error approving request:', error);
            showNotification('Error approving request', 'error');
        }
    };

    const handleReject = async (id) => {
        try {
            const response = await fetch(`http://localhost:8080/api/duty-exchange/${id}/reject`, {
                method: 'POST',
            });
            if (response.ok) {
                setExchanges(exchanges.map(ex =>
                    ex.id === id ? { ...ex, status: 'rejected', hodApproval: 'rejected' } : ex
                ));
                showNotification('Exchange request rejected.', 'error');
            } else {
                showNotification('Failed to reject request', 'error');
            }
        } catch (error) {
            console.error('Error rejecting request:', error);
            showNotification('Error rejecting request', 'error');
        }
    };

    const handleViewDetails = (exchange) => {
        setSelectedExchange(exchange);
        setShowModal(true);
        markAsRead(exchange.id);
    };

    const markAsRead = (id) => {
        setReadNotificationIds(prev => {
            const newSet = new Set(prev);
            newSet.add(id);
            return newSet;
        });
    };

    const markAllAsRead = () => {
        const allPendingIds = exchanges
            .filter(ex => ex.status === 'pending')
            .map(ex => ex.id);

        setReadNotificationIds(prev => {
            const newSet = new Set(prev);
            allPendingIds.forEach(id => newSet.add(id));
            return newSet;
        });
        setShowOptions(false);
    };

    const handleNotificationClick = () => {
        setShowNotifications(!showNotifications);
        if (showNotifications) setShowOptions(false);
    };

    const showNotification = (message, type) => {
        // You can implement a toast notification here
        alert(message);
    };

    // Filter notifications based on active tab
    const getFilteredNotifications = () => {
        const pendingExchanges = exchanges.filter(ex => ex.status === 'pending');
        if (activeTab === 'Unread') {
            return pendingExchanges.filter(ex => !readNotificationIds.has(ex.id));
        }
        return pendingExchanges;
    };

    const unreadCount = exchanges.filter(ex => ex.status === 'pending' && !readNotificationIds.has(ex.id)).length;

    const filteredExchanges = exchanges.filter(ex => {
        const statusMatch = filter === 'all' || ex.status === filter;

        if (searchQuery === '') {
            return statusMatch;
        }

        const query = searchQuery.toLowerCase().trim();
        const requestedBy = (ex.requestedBy || '').toLowerCase();
        const exchangeWith = (ex.exchangeWith || '').toLowerCase();

        const searchMatch = requestedBy.includes(query) || exchangeWith.includes(query);
        return statusMatch && searchMatch;
    });

    const formatDate = (dateString) => {
        const [year, month, day] = dateString.split('-');
        return `${day}-${month}-${year}`;
    };

    const formatDuty = (dutyString) => {
        // Format: "MSE - 2024-02-20" -> "MSE - 20-02-2024"
        const parts = dutyString.split(' - ');
        if (parts.length === 2) {
            const [year, month, day] = parts[1].split('-');
            return `${parts[0]} - ${day}-${month}-${year}`;
        }
        return dutyString;
    };

    const getStatusBadge = (status) => {
        const statusClasses = {
            pending: 'status-badge pending',
            approved: 'status-badge approved',
            rejected: 'status-badge rejected'
        };
        return <span className={statusClasses[status]}>{status.toUpperCase()}</span>;
    };

    return (
        <div className="management-container">
            <header className="management-header">
                <div className="header-content">
                    <h1>DUTY EXCHANGE SYSTEM</h1>
                    <p className="subtitle">Faculty Exchange Management Portal</p>
                </div>
                <div className="header-actions" style={{ position: 'relative' }}>
                    <button
                        className="btn-new-request"
                        onClick={() => navigate('/form')}
                    >
                        + New Exchange Request
                    </button>
                    <button
                        className="btn-notification"
                        onClick={handleNotificationClick}
                        title="Notifications"
                    >
                        <svg viewBox="0 0 24 24" fill="currentColor" width="24" height="24">
                            <path d="M12 22c1.1 0 2-.9 2-2h-4c0 1.1.9 2 2 2zm6-6v-5c0-3.07-1.63-5.64-4.5-6.32V4c0-.83-.67-1.5-1.5-1.5s-1.5.67-1.5 1.5v.68C7.64 5.36 6 7.92 6 11v5l-2 2v1h16v-1l-2-2zm-2 1H8v-6c0-2.48 1.51-4.5 4-4.5s4 2.02 4 4.5v6z" />
                        </svg>
                        {unreadCount > 0 && (
                            <span className="notification-badge">
                                {unreadCount}
                            </span>
                        )}
                    </button>

                    {showNotifications && (
                        <div className="notification-dropdown">
                            <div className="notification-header">
                                <h3>Notifications</h3>
                                <div style={{ position: 'relative' }}>
                                    <button
                                        className="btn-icon"
                                        onClick={(e) => {
                                            e.stopPropagation();
                                            setShowOptions(!showOptions);
                                        }}
                                    >
                                        â€¢â€¢â€¢
                                    </button>
                                    {showOptions && (
                                        <div className="options-menu">
                                            <button
                                                className="options-item"
                                                onClick={(e) => {
                                                    e.stopPropagation();
                                                    markAllAsRead();
                                                }}
                                            >
                                                <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                                                    <polyline points="20 6 9 17 4 12"></polyline>
                                                </svg>
                                                Mark all as read
                                            </button>
                                        </div>
                                    )}
                                </div>
                            </div>
                            <div className="notification-tabs">
                                <button
                                    className={activeTab === 'All' ? 'active' : ''}
                                    onClick={() => setActiveTab('All')}
                                >
                                    All
                                </button>
                                <button
                                    className={activeTab === 'Unread' ? 'active' : ''}
                                    onClick={() => setActiveTab('Unread')}
                                >
                                    Unread
                                </button>
                            </div>
                            <div className="notification-list">
                                {getFilteredNotifications().length === 0 ? (
                                    <div className="no-notifications">No {activeTab === 'Unread' ? 'unread' : 'new'} notifications</div>
                                ) : (
                                    getFilteredNotifications().map(ex => (
                                        <div key={ex.id} className="notification-item" onClick={() => {
                                            handleViewDetails(ex);
                                            setShowNotifications(false);
                                        }}>
                                            <div className="notification-avatar">
                                                {ex.requestedBy.charAt(0)}
                                            </div>
                                            <div className="notification-content">
                                                <p>
                                                    <strong>{ex.requestedBy}</strong> requested a duty exchange.
                                                </p>
                                                <span className="notification-time">{ex.date}</span>
                                            </div>
                                            {!readNotificationIds.has(ex.id) && (
                                                <div className="notification-indicator"></div>
                                            )}
                                        </div>
                                    ))
                                )}
                            </div>
                        </div>
                    )}
                </div>
            </header>

            <div className="management-content">
                {/* Stats Cards */}
                <div className="stats-grid">
                    <div className="stat-card">
                        <div className="stat-icon pending-icon">ðŸ“‹</div>
                        <div className="stat-info">
                            <h3>{exchanges.filter(ex => ex.status === 'pending').length}</h3>
                            <p>Pending Requests</p>
                        </div>
                    </div>
                    <div className="stat-card">
                        <div className="stat-icon approved-icon">âœ“</div>
                        <div className="stat-info">
                            <h3>{exchanges.filter(ex => ex.status === 'approved').length}</h3>
                            <p>Approved</p>
                        </div>
                    </div>
                    <div className="stat-card">
                        <div className="stat-icon rejected-icon">âœ•</div>
                        <div className="stat-info">
                            <h3>{exchanges.filter(ex => ex.status === 'rejected').length}</h3>
                            <p>Rejected</p>
                        </div>
                    </div>
                    <div className="stat-card">
                        <div className="stat-icon total-icon">ðŸ“Š</div>
                        <div className="stat-info">
                            <h3>{exchanges.length}</h3>
                            <p>Total Requests</p>
                        </div>
                    </div>
                </div>

                {/* Filter Tabs & Search */}
                <div className="filter-search-container">
                    <div className="filter-tabs">
                        <button
                            className={filter === 'all' ? 'tab active' : 'tab'}
                            onClick={() => setFilter('all')}
                        >
                            All ({exchanges.length})
                        </button>
                        <button
                            className={filter === 'pending' ? 'tab active' : 'tab'}
                            onClick={() => setFilter('pending')}
                        >
                            Pending ({exchanges.filter(ex => ex.status === 'pending').length})
                        </button>
                        <button
                            className={filter === 'approved' ? 'tab active' : 'tab'}
                            onClick={() => setFilter('approved')}
                        >
                            Approved ({exchanges.filter(ex => ex.status === 'approved').length})
                        </button>
                        <button
                            className={filter === 'rejected' ? 'tab active' : 'tab'}
                            onClick={() => setFilter('rejected')}
                        >
                            Rejected ({exchanges.filter(ex => ex.status === 'rejected').length})
                        </button>
                    </div>

                    <div className="search-container">
                        <input
                            type="text"
                            placeholder="Search by name..."
                            value={searchQuery}
                            onChange={(e) => setSearchQuery(e.target.value)}
                            className="search-input"
                        />
                        <svg className="search-icon" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round">
                            <circle cx="11" cy="11" r="8"></circle>
                            <line x1="21" y1="21" x2="16.65" y2="16.65"></line>
                        </svg>
                    </div>
                </div>

                {/* Exchanges Table */}
                <div className="table-container">
                    <table className="exchanges-table">
                        <thead>
                            <tr>
                                <th>ID</th>
                                <th>Requested By</th>
                                <th>Allotted Duty</th>
                                <th>Exchange With</th>
                                <th>Exchange Duty</th>
                                <th>Request Date</th>
                                <th>Status</th>
                                <th>Actions</th>
                            </tr>
                        </thead>
                        <tbody>
                            {filteredExchanges.length === 0 ? (
                                <tr>
                                    <td colSpan="8" className="no-data">
                                        No exchange requests found
                                    </td>
                                </tr>
                            ) : (
                                filteredExchanges.map((exchange) => (
                                    <tr key={exchange.id}>
                                        <td>{exchange.readableId}</td>
                                        <td>{exchange.requestedBy}</td>
                                        <td>{formatDuty(exchange.allottedDuty)}</td>
                                        <td>{exchange.exchangeWith}</td>
                                        <td>{formatDuty(exchange.exchangeDuty)}</td>
                                        <td>{formatDate(exchange.date)}</td>
                                        <td>{getStatusBadge(exchange.status)}</td>
                                        <td className="actions-cell">
                                            <button
                                                className="btn-view"
                                                onClick={() => handleViewDetails(exchange)}
                                                title="View Details"
                                            >
                                                <svg
                                                    width="16"
                                                    height="16"
                                                    viewBox="0 0 24 24"
                                                    fill="none"
                                                    stroke="currentColor"
                                                    strokeWidth="2"
                                                    strokeLinecap="round"
                                                    strokeLinejoin="round"
                                                >
                                                    <path d="M1 12s4-8 11-8 11 8 11 8-4 8-11 8-11-8-11-8z"></path>
                                                    <circle cx="12" cy="12" r="3"></circle>
                                                </svg>
                                            </button>
                                            {exchange.status === 'pending' && (
                                                <>
                                                    <button
                                                        className="btn-approve"
                                                        onClick={() => handleApprove(exchange.id)}
                                                        title="Approve"
                                                    >
                                                        âœ“
                                                    </button>
                                                    <button
                                                        className="btn-reject"
                                                        onClick={() => handleReject(exchange.id)}
                                                        title="Reject"
                                                    >
                                                        âœ•
                                                    </button>
                                                </>
                                            )}
                                        </td>
                                    </tr>
                                ))
                            )}
                        </tbody>
                    </table>
                </div>
            </div>

            {/* Detail Modal */}
            {showModal && selectedExchange && (
                <div className="modal-overlay" onClick={() => setShowModal(false)}>
                    <div className="modal-box" onClick={(e) => e.stopPropagation()}>
                        <div className="modal-header">
                            <h2>Exchange Request Details</h2>
                        </div>
                        <div className="modal-body">
                            <div className="detail-row">
                                <span className="detail-label">Request ID:</span>
                                <span className="detail-value">{selectedExchange.readableId}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">Requested By:</span>
                                <span className="detail-value">{selectedExchange.requestedBy}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">Allotted Duty:</span>
                                <span className="detail-value">{formatDuty(selectedExchange.allottedDuty)}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">Exchange With:</span>
                                <span className="detail-value">{selectedExchange.exchangeWith}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">Exchange Duty:</span>
                                <span className="detail-value">{formatDuty(selectedExchange.exchangeDuty)}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">Request Date:</span>
                                <span className="detail-value">{formatDate(selectedExchange.date)}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">HOD Approval:</span>
                                <span className="detail-value">{getStatusBadge(selectedExchange.hodApproval)}</span>
                            </div>
                            <div className="detail-row">
                                <span className="detail-label">Current Status:</span>
                                <span className="detail-value">{getStatusBadge(selectedExchange.status)}</span>
                            </div>
                        </div>
                        <div className="modal-footer">
                            {selectedExchange.status === 'pending' && (
                                <>
                                    <button
                                        className="btn-modal-approve"
                                        onClick={() => {
                                            handleApprove(selectedExchange.id);
                                            setShowModal(false);
                                        }}
                                    >
                                        Approve Request
                                    </button>
                                    <button
                                        className="btn-modal-reject"
                                        onClick={() => {
                                            handleReject(selectedExchange.id);
                                            setShowModal(false);
                                        }}
                                    >
                                        Reject Request
                                    </button>
                                </>
                            )}
                            <button
                                className="btn-modal-close"
                                onClick={() => setShowModal(false)}
                            >
                                Close
                            </button>
                        </div>
                    </div>
                </div>
            )}
        </div>
    );
};

export default DutyExchangeManagement;
